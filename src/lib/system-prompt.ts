// 生成带实时数据的系统提示
export function generateSystemPromptWithContext(context: {
  prices?: string
  inventory?: string
  date?: string
  knowledgeGraph?: string
}): string {
  let contextSection = '\n\n---\n## 📡 实时市场数据\n\n'

  if (context.date) {
    contextSection += `**数据更新时间**：${context.date}\n\n`
  }

  if (context.prices) {
    contextSection += `### 最新价格数据\n${context.prices}\n\n`
  }

  if (context.inventory) {
    contextSection += `### 库存概况\n${context.inventory}\n\n`
  }

  if (context.knowledgeGraph) {
    contextSection += `### 知识图谱分析\n${context.knowledgeGraph}\n\n`
  }

  contextSection += '**请在回答中引用以上实时数据和知识图谱洞察，确保分析的准确性和时效性。展示清晰的因果推理链条。**'

  return SYSTEM_PROMPT + contextSection
}